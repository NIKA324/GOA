print(not (10 < 5))

# 5. რამდენიმე შედარება ერთად
print(5 <= 5 and 10 >= 10 and 3 != 2)

# 6. or ოპერატორი სხვადასხვა ტიპებზე
print(10 > 100 or "apple" == "apple")

# 7. not და or
print(not (5 > 10 or 2 < 1))

# 8. მათემატიკური მოქმედება და ლოგიკა
print(5 + 5 == 10 and 2 * 2 == 4)

# 9. მკაცრი და არამკაცრი შედარება
print(7 > 6 and 8 <= 8)

# 10. რთული ლოგიკური გამოსახულება
print((10 != 10) or (5 > 2 and 3 < 10))


