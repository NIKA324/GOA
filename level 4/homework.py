# 1. and ოპერატორი - ორივე პირობა ჭეშმარიტია
print(True and True) # True

# 2. and ოპერატორი - ერთი პირობა მცდარია
print(True and False) # False

# 3. or ოპერატორი - მინიმუმ ერთი ჭეშმარიტია
print(True or False) # True

# 4. or ოპერატორი - ორივე მცდარია
print(False or False) # False

# 5. not ოპერატორი - აბრუნებს საწინააღმდეგოს
print(not True) # False

# 6. not ოპერატორი მცდარზე
print(not False) # True

# 7. კომბინაცია: and და or
print((True or False) and True) # True

# 8. შედარება და ლოგიკა
print(5 > 3 and 10 < 20) # True

# 9. პირობა, რომელიც მცდარია
print(10 == 10 and 5 > 10) # False

# 10. რთული ლოგიკური ჯაჭვი
print(not (5 == 5 and 3 > 1)) # False


